using System;

class PrintNumbers
{
    static void Main()
    {
        // Declare 3 new integer variables and give them values
        int a = 1;
        int b = 101;
        int c = 1001;

        // Print all the 3 integers each at a separate line
        Console.WriteLine("{0}\n{1}\n{2}", a, b, c);
    }
}